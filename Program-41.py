# Write a Python program to remove the first item from a specified list.

emp_name = ["avinesh", "sandeep", "priyanka", "shreyasi"]

print("Before Removing ", emp_name)

del emp_name[0]

print("After Removing ", emp_name)

'''
Before Removing  ['avinesh', 'sandeep', 'priyanka', 'shreyasi']
After Removing  ['sandeep', 'priyanka', 'shreyasi']
'''